            <h2 class="title">2012年8月8日</h2>
                <p>
               妹啊搬轉來厝也四冬了，時間也真快！<br>
                今日回家講伊答應阿弟求婚，我和阿志心內都很歡喜，又有一絲啊不甘。<br>
                <br>
                這幾年發生很多事，不過也給我知影了阿爸欸代誌......自細漢到老，心內一直結歸丸的煩惱，總算是可以放下。阿弟和伊欸基金會擱在無閒，因為擱有很多像阮這樣的人，擱無知影厝內失蹤欸人是給人關了後槍決，也無知影留落來的批......<br>
                <br>
                時代在向前行，不過阮還是希望阮阿爸這款代誌未擱發生在世界上......<br>
                雖然阮總算可以放下，也不得不放下，但是阮絕對未未記......<br>
                <br>

                哪是未記，阮驚這款代誌會擱重來一次，到時，擱有很多人欸爸母、兄弟姊妹、孩子欸失蹤，到死都不知人走去哪......安奈實在太悲哀了......<br>
                <br>

                妹啊講阿弟欸基金會要訪問阮這款『批給藏起』欸人，像是要出一本冊，給社會知影有這款代誌發生過。<br>
                <br>

                阮不敢給人訪問，不過，阮向妹啊講，若是冊開始賣了，阮一定會去買10本，送給少年囝仔讀。希望彼個時代過去了後，以後欸人也會永遠記得這款代誌，永遠未再讓這款代誌發生了！
                </p>

