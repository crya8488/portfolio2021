        <h2 class="top-subtitle">回顧那一段失落的生命</h2>
        <p class="top-description">
        你聽過「轉型正義」一詞嗎？轉型正義並非只討論過去威權體制的對與錯，更重要的是使我們擁有了解過去那些感情、那些信念的機會——在那個時空中，許多人確實冒險過、經歷過。
        不見得要討論民族大義，但想去珍惜那一段段、活生生的生命軌跡。因此我們參考了《無法送達的遺書》與其他文獻資料於下方呈現，希望能藉由這故事，訴說臺灣過去的這段歷史。<br>
