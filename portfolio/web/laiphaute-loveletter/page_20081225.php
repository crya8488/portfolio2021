            <h2 class="title">2008年12月25日</h2>
                <p>
               今日妹啊下班買了漂亮的雞卵糕和炸雞桶，說祝我和阿爸聖誕節快樂。阿志嘴一直嫌，可是還是吃得很歡喜，這個查甫就是這樣，明明很歡喜女兒這麼懂事。<br>
               <br>
               妹啊很乖，但是我總是很煩惱她太乖會被騙。和我不同款，不喜歡參加一些有的沒的，也沒有尬意欸查埔，安奈甘好？<br>
               不過最近有一些生分人在厝邊頭尾出入，隔壁金枝說常常看到妹啊和一個查甫在講話，伊是誰呢......？<br>
               我有點思念阿母......若是阿母在天上也能吃到雞卵糕就好了。聽說阿母以前是千金小姐，為了嫁阿爸才出走。誰知道阿爸後來會失蹤呢......阿母過身之前也不知人了，看了實在很不甘。若是阿爸也在天上了，阮希望伊這回欸好好疼惜阿母，未擱走無人啊。<br>
               若是我和妹啊同款聽話，阿爸甘會就一直陪在阿母和阮身邊？
                </p>

