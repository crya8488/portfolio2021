            <h2 class="title">2011年7月15日</h2>
                <p>
               今日記者會，政府總算會失禮啊......看到總統道歉，將批還給黃桑一家伙彼當時，我就忍不住直直流目屎。<br>
               <br>
               阿志給我攬緊緊，我看到阿弟偷偷拿一張棉仔紙給妹啊，就親像阿爸也是一個溫柔欸查甫，阿母才會嫁給伊吧......不過，這款溫柔，狠狠就給人拿無去......不知阿母甘會後悔少年時跟阿爸走？不知每一次我給人欺負沒阿爸，心內怨嘆阿爸是將阮母子放撒欸浪子時，阿爸在天上甘會委屈？給人關、知影自己會給槍殺的時候，給阿母和阮寫批，安慰阮時，心內又是安怎痛苦、安怎驚惶？<br>
               <br>
               想到這，我心內就號未離......
                </p>

