                        公文往返一年多後，有鑒於檔案局遲遲不肯全面清查受難者的遺書，又一直沒有建立遺書返還制度。我們於二○一一年二月與黃溫恭的子女黃大一及黃春蘭一同召開記者會，提出三點訴求：
                    </p>
                    <!-- 展開內容 -->
                    <p class="source-more-content"> 
                        一、遺書由總統代表國家親自返還；二、檔案局應為遺書返還建立機制；三、檔案局應主動全面清查檔案中是否還有受難者的遺書，主動通知家屬領回，而不是被動等待家屬申請。第三點尤其重要，當年政府扣留了受難者的遺書，許多家屬根本無從得知遺書存在，這樣如何可能主動申請？一個月後，研考會回應我們的訴求，表示將擬定相關程序，建立受難者家書返還的相關制度。二○一一年七月十五日，在解除戒嚴二十四年後，馬英九總統在「戒嚴時期政治受難者紀念儀式」上歸還黃溫恭及其他受難者的遺書，並對家屬致歉，也在致詞時表示檔案局將清查二百多萬頁的檔案，找出受難者的家書、筆記與手札歸還給家屬。二○一一年下半年，檔案局總共清查出一百七十七位受難者的私人文書，共約七百四十七頁。
