                    三月空襲發生後，被美軍轟炸威力嚇壞的臺南人紛紛往鄉下避難。我父親少時陪他獨居的外嬤住在臺南高砂町（光復後改名建國路，現在的民權路一段），空襲後隔日天一亮外嬤立即徒步往北疏開
                    </p>
                    <p class="source-more-content">
                    雖然標題寫「奔逃」，是說逃命的心情，實際上她因為裹小腳，走得極為緩慢，當天父親人在鄉下，傍晚才聽說臺南發生空襲，焦急的往臺南走去，沿路在疏開的人群裡尋找外嬤蹤影，直到晚上九點多終於在曾文溪畔君代橋頭找到他外嬤。<br>
                    <span class="line"></span>
                    1945年八月日本天皇宣佈投降後，對臺灣人來說最直接的改變就是再也不用躲空襲了，父親獨自啟程回臺南，在三月清晨踩著小腳辛苦地和他一步一步走回鄉下的外嬤未能撐過瘧疾，沒有回到臺南高砂町的家。<br>
                    我父親是個性格強靭的人，回憶及他外嬤因為瘧疾死在鄉下時，儘管已是六十幾年前的往事，眼神依然流洩當時無力救助之深沈悲傷，空襲之驚恐猶如昨日歷歷。戰爭留下的創痛，也許曾暫時塵封於歲月的腳步下，卻從未自他們心底消失。<br>
                    回到臺南隔天，父親立即趕到學校去，學校並沒有通知學生復課，但學生們都不約而同自動回學校，因為已經很久未曾上課了，思念學校。<br>
                    那個沒有事先約定的返校日，日本老師看到許久不見的學生，悲喜交雜。時代變了，日本老師說中國政府即將派人來接收學校，少年學生們沈浸在戰爭結束和重回祖國懷抱的複雜喜悅裡，渾然不知另一波更血腥的劫難和白色恐怖大霧即將籠罩臺灣……<br>
