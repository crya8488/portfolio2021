                <p>
                   三月二十一日　晴<br>
                    今日於菜市買些米和肉菜、錢就咧欲用光了家用愈來愈無夠、卻非開銷變大、是物件一直起價。猶記戰時是漸漸起的、但從舊年光復以來米價已猛翻三倍以上、明明戰爭攏過了啊，政府在做啥？新新上華王兒的漫畫亦使人受氣、人言狗去豬來無錯、想到主人薪水未見起色、煩惱去哪尋手工來做。<br>
